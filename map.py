#------------------Class Map----------------
class map:
    def __init__(self, img, rect):
        self.img = img
        self.rect = rect
    def draw(self,scr):
        scr.blit(self.img,self.rect)
def drawMap(screen):
    blockgroup.clear()
    blockimg = pygame.image.load('img/block.png').convert_alpha()
    for i in range(10):
        for j in range(10):
            if blockgroup[i][j] == 1:
                block = map(blockimg,Rect(30*j, 30*i, 30, 30))  # 初始化障碍物方块
                block.draw(screen)  # 绘制障碍物方块
                block_group.append(block)  # 向列表中加入该方块
    
